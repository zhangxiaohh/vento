function svgBufferToPngBuffer(svg) {
  const resvg = new Resvg(svg);
  const pngData = resvg.render();
  return new Uint8Array(pngData.asPng());
}
async function generateOgImageForPost(post, localeKey, localeConfig) {
  const svg = await postOgImage(post, localeKey, localeConfig);
  return svgBufferToPngBuffer(svg);
}
async function generateOgImageForSite(localKey, localeConfig) {
  const svg = await siteOgImage(localKey, localeConfig);
  return svgBufferToPngBuffer(svg);
}

